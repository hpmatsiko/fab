    <h3>⦁   Envisioning the Future – The EBE Strategic Plan 2024-2029 </h3>
    Since its beginning in 1962, the EBS has been working under the shadow of Inkurunziza Church and because of that, it does not have its own legal document authorizing its operations in the country. Over time, the lack of legal authorization documents has created confusion in the administration and management of EBS, especially in their relationship with Inkurunziza (as the administrative host), and with other denominations and faith-based organizations. These challenges have reached their pick in 2021 when the Inkurunziza Leadership decided to relocate its national administrative offices including the EBS office due to renovation constructions that were planned to last for three years’ time.
    It is in that perspective that the EBS leadership has realized that it is high time to put in place a strategy plan to respond to all these challenges. The following are the highlights of the key pillars and objectives that will be included in the strategic plan.

    <h3>1.  EBE MINISTRY DEVELOPMENT </h3>
    ⦁   Create and strengthen existing EBE Centres and Sub-Centres to teach and disseminate the word of God through distance education in the form of Bible courses and other material.
    ⦁   Develop the EBE online course – using Kinyarwanda language (the online application is available in English, French, and Kiswahili)
    ⦁   Strengthen Radio ministry – to make it a unique strategy for evangelization, teaching the Word of God and sensitization for new students’ enrolment. 
    ⦁   Prison ministry – empowering (including regular retreats and refresher training) for all the volunteers serving in prison ministry and set a strong and sustainable mechanism of our the EBE ministry in Prison.
    <h3>2.  EBE LEADERSHIP DEVELOPMENT</h3>
    ⦁   Ministry Leaders empowerment – Recruitment, Equipment, Motivation, and Retention of Ministry leaders
    ⦁   Improving Ministry Delivery – Develop (and print out for use) of the EBE Ministry Manual in Kinyarwanda to organize the functioning and management of centres and sub-centres across the country.
    ⦁   Promote EBE Volunteering – Set in place strong and sustainable strategies to create passion for volunteering in EBE ministry (through EBE promotion week, materials, radio talk, etc.)
    ⦁   Empowerment of Staff and Volunteers – Set in place regular refresher training and ministry retreat for EBE leaders and volunteers (at least one retreat per year for ministry leaders, and one refresher training per year for ministry volunteers)

