        RADIO MINISTRY
        Radio evangelism is one of the ministries that has contributed in building early Christianity in Rwanda in the1960ies-1970ies. The pioneers of Emmaus Correspondence School (ECS) in Rwanda began with radio and distribution of literature/Christian tracts ministries as an entry point of evangelizing. Emmaus was the first radio ministry for the whole country, 1,5 hours in in Kinyarwanda (local language) and 1,5 hours in French every week. Like other Brethren ministries in the country, ECS was also jeopardized by the departure of missionary Jack Lacey. ECS Radio ministry succumbed pressure especially from the Protestant Council and, by 1979 Brethren opportunity to evangelize through radio was taken away and given to Protestant Council. 

    </div>
    <img src="/img/9.png" alt="">
</div>

<div>
    In 2005, Emmaus Bible School has had the privilege of being again able to teach the Scripture and evangelize through Umucyo Community Radio Station, a Christian radio broadcasting from Kigali. It began with a 60-minute slot in Kinyarwanda, three time a week and later twice a week. Many have come to know the Lord through our Radio broadcasts and many more enrolled the Emmaus Bible School through the same Radio program. However, even though we praise God for the reviving of ECS Radio ministry, we are far behind where was the same ministry in the 1960-1970ies whereby we used to have our own studio and materials to prepare radio broadcasts and full-time staff for ECS radio program. It is our prayer that our Lord and God of harvest will continue to support and sustain Evangelism through Correspondence School.
