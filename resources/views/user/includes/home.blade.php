    <h3>EMMAUS PRISON MINISTRY</h3>
    The same as radio program, prison ministry is one of ministries with which Brethen Mission started with in Rwanda in the 1962. In the first two decades of brethren ministry in Rwanda, Inkurunziza was coordinating all of Protestant evangelism programs especially in Kigali City, including radio and prison programs. Archives indicates that Inkurunziza (Brethren) Church was the one and only church with authirzation to evangelize in Rwandan prisons in the 1960-1970ies. For example, in her field report of 1964-1967, 1977-1986; Miss Margaret Petrie, a Canadian missionary of Inkurunziza/Brethen Chruch wrote “We had the joy of baptizing two prisoners from the local jail. They are among those who have been studying the Emmaus courses. Special permission was given for them to come to a river just outside of town [probably at Nyabugogo], accompanied by a guard.”

</div>
<img src="/img/10.png" alt="">
</div>
<div>
    In 1970, the Emmaus Prison Ministry had to be closed due to the departure of Jack Lacey and Margaret Petrie the main initiators and supporters of the ministry. However, after the 1994 Genocide against the Tutsi, the prison ministry was reactivated. The Rwanda EBE has been accepted by the Rwanda Correctional Service (RCS) as providing a suitable teaching to prisoners. Through the co-operation of the Rwanda Prison Fellowship, Emmaus Bible School courses are being studied and completed by more than 800 students in over 7 correctional centres in Rwanda (Prison). Emmaus staff and some Brethren pastors are allowed to visit the prisons to preach the gospel and introduce the EBE courses. Over the last five years (2018-2022), there were 16,622 courses sent out to all the 7 correctional centres of the country. 674 earned their certificates having completed the first twelve courses while 423 received diplomas (having completed 24 courses), moreover, more than 1800 people shown their commitment to Jesus by being baptised.
