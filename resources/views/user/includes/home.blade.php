    <h2>EMMAUS CORRESPONDENCE SCHOOL</h2>
    As we have seen above, the Emmaus ministry was brought by John E Lacey (Jack) in 1962 as a ministry of Inkuru Nziza Association. Jack Lacey (+2002) and Nyamukama Michel (+2008) had been using Emmaus courses in connection with Radio ministry since 1958 while they ministered in Bujumbura/Burundi. In 1961 they moved to Kigali (Rwanda) and built a Centre of Christian Information (known as Foyer Inkuru Nziza) where they distributed books/Courses written in English, French and Kiswahili to a few numbers of people in the city of Kigali.
    It is not well known when the first lessons in Kinyarwanda started and who the first students were, however it is known that since 1965, some courses, such as “what the Bible Teaches” and “the servant of God” were translated in Kinyarwanda as well as a number of tracts. 
    

</div>
</div>
<div>In 1970, when Jack Lacey returned to his home country, the ministry of Emmaus, especially the Kinyarwanda courses, went slowly until two missionaries, miss. Margaret Petrie (1977) and Robert Cullen (1979) come in Rwanda and dedicated themselves to the ministry. Together with local volunteers, they made available 8 Emmaus courses in Kinyarwanda. In 1982, the number of Emmaus students grown up to 80 students who took a EBE course, with 48 courses available in three languages: Kinyarwanda (8), Swahiri (4), French (12) and English 24. In 1988, Robert Cullen moved to Kabare (Uganda) where he started a fruitful Emmaus ministry.
    In 1990, Mr Peter Andrews became a missionary at Inkuru Nziza church. He put a big effort in Emmaus ministry.  During his time, more than 2000 students were enrolled in EBE, with 24 courses translated in Kinyarwanda and 3 staffs were at EBE office in Kigali. While Peter Andrews continued to serve Emmaus Correspondence School as a full-time missionary, in 1998 he returned to his home country (UK) and then continued to serve but as a visiting missionary. 
    In 2003, Mr Sam Muhire took over the coordination of Emmaus ministry. In addition to existing Emmaus activities, he revived the key ministries of Emmaus ministry in the first two decencies of their ministry in Rwanda: Radio ministry and Prison ministry. 
    In 2019, 7,474 students (including 170 in prisons) were enrolled in EBE, 44 courses translated into Kinyarwanda, 15 EBE Centres were operational organized in four regions (Kigali Region, Southern Region, Western Region, northern Region and Eastern Region).</div>
<div>
    Today Emmaus Bible School in Rwanda has 49 courses translated in Kinyarwanda. With every course completed successfully a student receives a report card with the name of course completed, the percentage earned and the date. Upon accomplishment of the 12 courses, the student receives a certificate and when a further 12 are completed, the students receive a diploma. Over the last three years (2020-2022), there were 46,762 courses sent out to all corners of the country with 38,337 being returned for making. A number of our students has a good commitment in that 1,179 earned their certificates having completed the first twelve courses while 1,027 received diplomas (having completed 24 courses).
