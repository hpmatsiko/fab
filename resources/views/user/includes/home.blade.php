        <h3>Introduction</h3>
        Emmaus Bible School in Rwanda is a branch of the Emmaus Correspondence School Ministries, an international Bible correspondence school, non-denominational in outreach whose purpose is to encourage people to the systematic study of the word of God. 
        It gets its name from the Bible, in Luke 24:13-35, where two of Jesus’ disciples were walking along the road from Jerusalem to Emmaus discussing all that happened after the Lord’s death on the Cross. The Lord joined them in their walk and expounded unto them in all the scriptures the things concerning himself (Luke 24.27), and opened to them the scriptures (Luke 24.32).
        The Emmaus Bible School worldwide is committed to help people from every nation who would seek to know more of God’s word to “open” the scriptures by providing biblical sound resources and structured bible courses so that they may accept Christ as their Saviour and Lord, grow in Christ, and share their faith with others.
        From its small beginnings in 1940s in Toronto (Canada), the Emmaus Bible correspondence school has grown and become the largest bible based correspondence school in the world.  Today, over 1 million courses are distributed annually in more than 100 countries around the world, and in over 125 languages.  A number of missionaries and churches have made Emmaus courses a vital part of their evangelistic and bible teaching ministries.<br>
     <h3>Historical background of Emmaus Bible School in Rwanda</h3>
       

    </div>
</div>
<div>   
        In 1935, a Canadian Brethren missionary Robert Edward Harlow and his wife, Margaret, set sail from Toronto, their home town, to Belgium Congo and spent some time there as missionaries. Upon returning to Canada Harlow saw a real need for a school in North American where principles of New Testament Christianity can be taught without compromise. </div>
<div style="display: flex;
justify-content: center;
align-items: center;">
<div style="width: 110vh;">
    Then after, Harlow left his Congo mission station and in collaboration with his two friends, John Smart and C. Ernest Tatham, began the organizing of Emmaus Bible School which opened evening classes in 1941. Aware that people wanted to study God’s Word, but not everyone could attend the evening classes, and in order to accommodate those individuals from outside of Toronto area, the Emmaus Bible School decided to send lessons to them in the form of correspondence courses since 1942.
    The correspondence courses were soon widely in use all over the North America in Sunday Schools, Camps, and youth groups. Missionaries all over the world as well as nationals started using Emmaus courses in their evangelical and biblical ministries. It is with this historical background that the Emmaus Bible School was brought in Rwanda by Brethren missionaries.
    ⦁   Historical overview of the Emmaus Bible School in Rwanda
    Emmaus Bible School (EBS) was introduced in Rwanda in 1962 by John E Lacey (Jack), an American brethren missionary, as one of key pillars of his evangelism ministry in Rwanda with the purpose to introduce people to a personal knowledge and relationship with Jesus Christ as their saviour and Lord.

